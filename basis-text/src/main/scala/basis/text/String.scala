package basis.text

import basis._
import basis.util._

object String {
  def apply(chars: java.lang.CharSequence): UTF8String = UTF8String(chars)
  
  private[text] def expand(base: Int, size: Int): Int = {
    var n = (base max size) - 1
    n |= n >> 1; n |= n >> 2; n |= n >> 4; n |= n >> 8; n |= n >> 16
    n + 1
  }
}
