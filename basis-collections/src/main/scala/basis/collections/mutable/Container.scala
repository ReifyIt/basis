package basis.collections
package mutable

/** A mutable container.
  * 
  * @groupprio  Examining     -4
  * @groupprio  Iterating     -3
  * @groupprio  Traversing    -2
  * @groupprio  Classifying   -1
  * 
  * @define collection  container
  */
trait Container[A]
  extends Any
    with Mutable
    with Family[Container[A]]
    with Collection[A]
    with traversable.Container[A]
